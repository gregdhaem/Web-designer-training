/**
 * 
 *          INCREMENTATIONS ET DECREMENTATIONS
 * 
 * 
 *              INCREMENTATIONS
 * 
 */

var nb1 = 1;
console.log(nb1);
nb1 = nb1 + 1;
console.log(nb1);
nb1 += 1;
console.log(nb1);
// simplifié
nb1++;
console.log(nb1);



/**
 *  
 *              DEREMENTATIONS
 * 
 */

var nb1 = 1;
console.log(nb1);
nb1 = nb1 - 1;
console.log(nb1);
nb1 -= 1;
console.log(nb1);
// simplifié
nb1--;
console.log(nb1);

/**
 *  
 *              SUBTILITE
 * 
 */

console.log("-------------");

nb1 = 0;
console.log(nb1);
console.log(nb1++);
console.log(nb1);
console.log("-------------");
nb1 = 0;
console.log(nb1);
console.log(++nb1);
console.log(nb1);